package com.kdu.smarthome.constants;
/**
 * Enumeration representing user roles who are owners of house.
 * Each house owner can have one of the following roles: ADMIN or USER.
 */
public enum UserRole {
    /**
     * Represents the role of an admin of house.
     */
    ADMIN,

    /**
     * Represents the role of a regular owner of house.
     */
    USER
}
